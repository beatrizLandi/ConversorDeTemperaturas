
package CalculosDeConversaoDeTemperatura;

 
public class CelsiusConversor {
    //atributos
    
    //metodos
    //------------------------------------------------------------------------
    double celsiuParaFahhrenheit(double celcius ){
        
        return 9.0 * celcius /5.0 +32.0 ;
    
    }
    //-------------------------------------------------------------------------
    double celsiuParaKelvin(double celcius ){
        
     return celcius + 273.15;
    
    }
    //-------------------------------------------------------------------

    
    
    
    
}
