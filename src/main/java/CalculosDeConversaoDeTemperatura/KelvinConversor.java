
package CalculosDeConversaoDeTemperatura;

public class KelvinConversor {
    
   //------------------------------------------------------------------------
    double kelvinParaCelcius (double kelvin ){
    
    return kelvin -237.15;
        
    }
    //-----------------------------------------------------------------
     double kelvinParaFarenheint (double kelvin){
    
        return ((kelvin - 273.15)* 1.8) +32;
    
    }
     //-------------------------------------------------------------------

}
